from bs4 import BeautifulSoup as bs
import requests
import random

url = "https://developers.whatismybrowser.com/useragents/explore/operating_system_name/windows/"
content = requests.get(url + str(random.randint(2,10)))
soup = bs(content.content,'html.parser')
USER_AGENTS = []
for cnt in soup.find_all("td",{"class":"useragent"}):
    USER_AGENTS.append(cnt.text)
